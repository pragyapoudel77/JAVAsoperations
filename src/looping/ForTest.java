package looping;

public class ForTest {
	/***
	 * 
	 *     --------for loop----------
	 *     
	 *     #syntax:
	 *     
	 *     for(initialization;condition;increment/decrement){    
	 *     	//statements  ;Condition is like end 
	 *     
	 *     }
	 * 
	 * 
	 * 
	 * 
	 * 
	 * 
	 * 
	 * 
	 */
	public static void main(String[] args) {   //String as argument 
		
		int i;
		int j=10;
		
		for(i=1;i<=500;i++) {
			
			System.out.println("Good Afternoon");
			System.out.println(j);
			
		}
	}
}
